largura = float(input("Define uma largura do retângulo: "))
altura = float(input("Define uma altura para o retângulo: "))
area = largura * altura
perimetro = (largura + altura) * 2
print("O retângulo de largura {} e altura {} tem uma área {} e um perímetro {}".format(largura, altura, area, perimetro))
